<template>
    <div class="image-container" v-if="isLoggedIn">
        <img v-for="image in getImages" :key="image.id" :src="image.link">        
    </div>
</template>

<script>
import { mapActions, mapGetters } from 'vuex';

export default {
    name: 'ImageList',
    computed: {
        ...mapGetters(['getImages', 'isLoggedIn']),
    },
    methods: {
        ...mapActions(['fetchImages']),
    },
    created() {
        this.fetchImages();
    }
}
</script>

<style scoped>
.image-container {
    column-count: 3;
    column-gap: 0;
}

img {
    max-width: 100%;
    padding: 5px;
}
</style>